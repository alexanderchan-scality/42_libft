#include "ft_general.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (!s1 && !s2)
		return (1);
	if (!s1)
		return (0);
	if (!s2)
		return (0);
	while (n-- && (*s1 || *s2))
	{
		if (*s1++ != *s2++)
			return (0);
	}
	return (1);
}
